package kalaiautomationdeveloper;

public class IfElse {

	public static void main(String[] args) {
    // if else it's nothing but either one condition true or false 
		int a = 10;
		int b = 20;
		
		
		if (a>b) {
			System.out.println("kalai");
		}
		else if (a<b) {
			System.out.println("vijay");
		}
		else {
			System.out.println("vani");
		}
		
		
		String c = "Jaya";
		String d = "jaya";
		
		if (c.equalsIgnoreCase(d)) {
			System.out.println(" both are equal ");
		}
		
		else {
			System.out.println(" both are not equal");
		}

	}

}
