package kalaiautomationdeveloper;

public class DoWhile {

	public static void main(String[] args) {
// if the condition is unsatisfied if do is one time print but condition is satisfied  do is 9 time print
   int i = 1;
   do {
	   System.out.println(i);
	   i++;
   }
   while (i>10);
	}

}
